# aprendendo a importar bibliotecas (modulos)
# floor - arredondadomento p baixo
# ciel - arredondamento p cima
# sqrt - raiz quadrada de um numero

# importando uma biblioteca de operadores matematicos
import math

num = int(input('Digite um número: '))

#math.sqrt é uma funcionalidade da biblioteca math para saber a raiz quadrada de um numero
raiz = math.sqrt(num)

print('A raiz quadrada de {} é igual á {:.2f}'.format(num, raiz))




